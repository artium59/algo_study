#include <bits/stdc++.h>

int main() {
    long long n, s = 0, z = 45678;
    scanf("%lld", &n);
    
    // 1st  2nd     3rd         4th            5th                             6th
    // 5 -> 5+7 -> 5+7+10 -> 5+7+10+13 -> 5+7+10+13+16
    //      5+7   5+7+7+3  5+7+7+3+7+3+3  5+7+7+3+7+3+3+7+3+3+3   5+7+7+3+7+3+3+7+3+3+3+7+3+3+3+3
    //          5+7(3-1)+3(1)  5+7(4-1)+3(1+2)   5+7(5-1)+3(1+2+3)         5+7(6-1)+3(1+2+3+4)
    
    for (int i = 1; i <= n-2; i++)
        s += i;
    
    printf("%lld", (long long)(5 + 7*(n-1) + 3*s)%z);
    
    return 0;
}